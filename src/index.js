function App() {
  return (
    <div className="container">
      <Header />
      <Menu />
      <Footer />
    </div>
  );
}
function Header() {
  return (
    <header className="header">
      <h1>PIZZA MENU</h1>
    </header>
  );
}
function Menu() {
  const numPizzas = pizzaData.length;
  return (
    <main className="menu">
      <h2>Our menu</h2>
      {numPizzas > 0 ? (
        <>
          <p>
            Authentic Italian Cuisine. 6 creative dishes to choose from. All{" "}
            from our store oven, all organic, all delicious.
          </p>
          <ul className="pizzas">
            {pizzaData.map((pizza) => (
              <li className={pizza.soldOut ? "pizza sold-out" : "pizza"}>
                <img src={pizza.photoName} alt={pizza.name} />
                <div>
                  <h3>{pizza.name}</h3>
                  <p>{pizza.ingredients}</p>
                  <p>{pizza.soldOut ? "SOLD OUT" : pizza.price}</p>
                </div>
              </li>
            ))}
          </ul>
        </>
      ) : (
        <p>
          We are still working on working on our menu. please com back later.
        </p>
      )}
    </main>
  );
}

function Footer() {
  const hour = new Date().getHours();
  const openHour = 12;
  const closeHour = 22;
  const isOpen = hour >= openHour && hour <= closeHour;
  return (
    <footer className="footer">
      {isOpen ? (
        <div className="order">
          <p>
            We're open from {openHour}:00 to {closeHour}:00. Come visit us or
            order online.
          </p>
          <button className="btn">Order</button>
        </div>
      ) : (
        <p>
          Present store is closed.We're happy to welcome you between {openHour}
          :00 and {closeHour}:00.
        </p>
      )}
    </footer>
  );
}

const root = ReactDOM.createRoot(document.getElementById("root"));
root.render(
  <React.StrictMode>
    <App />
  </React.StrictMode>
);
